<template>
  <div class="w-full h-full p-0 bg-transparent border-none shadow-none">
    <div class="relative flex-b mt-2.5 max-md:block max-md:mt-1">
      <div class="w-112 mr-5 max-md:w-full max-md:mr-0">
        <div class="art-card-sm relative p-9 pb-6 overflow-hidden text-center">
          <img class="absolute top-0 left-0 w-full h-50 object-cover" src="@imgs/user/bg.webp" />
          <img
            class="relative z-10 w-20 h-20 mt-30 mx-auto object-cover border-2 border-white rounded-full"
            src="@imgs/user/avatar.webp"
          />
          <h2 class="mt-5 text-xl font-normal">{{ userInfo.userName }}</h2>
          <p class="mt-5 text-sm">专注于用户体验跟视觉设计</p>

          <div class="w-75 mx-auto mt-7.5 text-left">
            <div class="mt-2.5">
              <ArtSvgIcon icon="ri:mail-line" class="text-g-700" />
              <span class="ml-2 text-sm">jdkjjfnndf@mall.com</span>
            </div>
            <div class="mt-2.5">
              <ArtSvgIcon icon="ri:user-3-line" class="text-g-700" />
              <span class="ml-2 text-sm">交互专家</span>
            </div>
            <div class="mt-2.5">
              <ArtSvgIcon icon="ri:map-pin-line" class="text-g-700" />
              <span class="ml-2 text-sm">广东省深圳市</span>
            </div>
            <div class="mt-2.5">
              <ArtSvgIcon icon="ri:dribbble-fill" class="text-g-700" />
              <span class="ml-2 text-sm">字节跳动－某某平台部－UED</span>
            </div>
          </div>

          <div class="mt-10">
            <h3 class="text-sm font-medium">标签</h3>
            <div class="flex flex-wrap justify-center mt-3.5">
              <div
                v-for="item in lableList"
                :key="item"
                class="py-1 px-1.5 mr-2.5 mb-2.5 text-xs border border-g-300 rounded"
              >
                {{ item }}
              </div>
            </div>
          </div>
        </div>
      </div>
      <div class="flex-1 overflow-hidden max-md:w-full max-md:mt-3.5">
        <div class="art-card-sm">
          <h1 class="p-4 text-xl font-normal border-b border-g-300">基本设置</h1>

          <ElForm
            :model="form"
            class="box-border p-5 [&>.el-row_.el-form-item]:w-[calc(50%-10px)] [&>.el-row_.el-input]:w-full [&>.el-row_.el-select]:w-full"
            ref="ruleFormRef"
            :rules="rules"
            label-width="86px"
            label-position="top"
          >
            <ElRow>
              <ElFormItem label="姓名" prop="realName">
                <ElInput v-model="form.realName" :disabled="!isEdit" />
              </ElFormItem>
              <ElFormItem label="性别" prop="sex" class="ml-5">
                <ElSelect v-model="form.sex" placeholder="Select" :disabled="!isEdit">
                  <ElOption
                    v-for="item in options"
                    :key="item.value"
                    :label="item.label"
                    :value="item.value"
                  />
                </ElSelect>
              </ElFormItem>
            </ElRow>

            <ElRow>
              <ElFormItem label="昵称" prop="nikeName">
                <ElInput v-model="form.nikeName" :disabled="!isEdit" />
              </ElFormItem>
              <ElFormItem label="邮箱" prop="email" class="ml-5">
                <ElInput v-model="form.email" :disabled="!isEdit" />
              </ElFormItem>
            </ElRow>

            <ElRow>
              <ElFormItem label="手机" prop="mobile">
                <ElInput v-model="form.mobile" :disabled="!isEdit" />
              </ElFormItem>
              <ElFormItem label="地址" prop="address" class="ml-5">
                <ElInput v-model="form.address" :disabled="!isEdit" />
              </ElFormItem>
            </ElRow>

            <ElFormItem label="个人介绍" prop="des" class="h-32">
              <ElInput type="textarea" :rows="4" v-model="form.des" :disabled="!isEdit" />
            </ElFormItem>

            <div class="flex-c justify-end [&_.el-button]:!w-27.5">
              <ElButton type="primary" class="w-22.5" v-ripple @click="edit">
                {{ isEdit ? '保存' : '编辑' }}
              </ElButton>
            </div>
          </ElForm>
        </div>

        <div class="art-card-sm my-5">
          <h1 class="p-4 text-xl font-normal border-b border-g-300">更改密码</h1>

          <ElForm :model="pwdForm" class="box-border p-5" label-width="86px" label-position="top">
            <ElFormItem label="当前密码" prop="password">
              <ElInput
                v-model="pwdForm.password"
                type="password"
                :disabled="!isEditPwd"
                show-password
              />
            </ElFormItem>

            <ElFormItem label="新密码" prop="newPassword">
              <ElInput
                v-model="pwdForm.newPassword"
                type="password"
                :disabled="!isEditPwd"
                show-password
              />
            </ElFormItem>

            <ElFormItem label="确认新密码" prop="confirmPassword">
              <ElInput
                v-model="pwdForm.confirmPassword"
                type="password"
                :disabled="!isEditPwd"
                show-password
              />
            </ElFormItem>

            <div class="flex-c justify-end [&_.el-button]:!w-27.5">
              <ElButton type="primary" class="w-22.5" v-ripple @click="editPwd">
                {{ isEditPwd ? '保存' : '编辑' }}
              </ElButton>
            </div>
          </ElForm>
        </div>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
  import { computed, onMounted, reactive, ref } from 'vue'
  import { ElMessage, type FormInstance, type FormRules } from 'element-plus'
  import { fetchCurrentUserProfile, updateUserProfile } from '@/api/user'
  import { fetchGetUserInfo } from '@/api/auth'
  import { useUserStore } from '@/store/modules/user'

  defineOptions({ name: 'UserCenter' })

  const userStore = useUserStore()
  const userInfo = computed(() => userStore.getUserInfo)

  const isEdit = ref(false)
  const isEditPwd = ref(false)
  const date = ref('')
  const ruleFormRef = ref<FormInstance>()

  const form = reactive({
    realName: '',
    nikeName: '',
    email: '',
    mobile: '',
    address: '',
    sex: '' as '' | number,
    des: ''
  })

  const pwdForm = reactive({
    password: '',
    newPassword: '',
    confirmPassword: ''
  })

  const rules = reactive<FormRules>({
    realName: [
      { required: true, message: 'Please enter your name', trigger: 'blur' },
      { min: 2, max: 50, message: 'Length 2 to 50 characters', trigger: 'blur' }
    ],
    nikeName: [
      { required: true, message: 'Please enter a nickname', trigger: 'blur' },
      { min: 2, max: 50, message: 'Length 2 to 50 characters', trigger: 'blur' }
    ],
    email: [{ required: true, message: 'Please enter email', trigger: 'blur' }],
    mobile: [{ required: true, message: 'Please enter mobile number', trigger: 'blur' }],
    address: [{ required: true, message: 'Please enter address', trigger: 'blur' }],
    sex: [{ required: true, message: 'Please choose gender', trigger: 'change' }]
  })

  const options = [
    { value: 1, label: 'Male' },
    { value: 0, label: 'Female' }
  ]

  const lableList: Array<string> = [
    'Design lover',
    'Idea maker',
    'Enjoy spicy',
    'Long legs',
    'Optimistic',
    'Inclusive'
  ]

  onMounted(() => {
    getDate()
    loadProfile()
  })

  const loadProfile = async () => {
    try {
      const profile = await fetchCurrentUserProfile()
      fillForm(profile)
    } catch (error) {
      console.error('Load profile failed:', error)
      fillForm(userInfo.value as any)
    }
  }

  const fillForm = (profile?: Partial<Api.Auth.ProfileDetail>) => {
    form.realName = profile?.name || profile?.userName || ''
    form.nikeName = profile?.nickname || profile?.userName || ''
    form.email = profile?.email || ''
    form.mobile = (profile as any)?.mobile || ''
    form.address = (profile as any)?.localAddress || ''
    form.sex = typeof profile?.sex === 'number' ? profile.sex : ''
  }

  const getDate = () => {
    const h = new Date().getHours()

    if (h >= 6 && h < 9) date.value = 'Good morning'
    else if (h >= 9 && h < 11) date.value = 'Morning'
    else if (h >= 11 && h < 13) date.value = 'Noon'
    else if (h >= 13 && h < 18) date.value = 'Afternoon'
    else if (h >= 18 && h < 24) date.value = 'Evening'
    else date.value = 'It is late, have a rest'
  }

  const edit = async () => {
    if (!isEdit.value) {
      isEdit.value = true
      return
    }

    if (!ruleFormRef.value) return

    await ruleFormRef.value.validate()

    const payload: Api.Auth.ProfilePayload = {
      name: form.realName || undefined,
      nickname: form.nikeName || undefined,
      email: form.email || undefined,
      mobile: form.mobile || undefined,
      localAddress: form.address || undefined,
      sex: form.sex === '' ? undefined : Number(form.sex)
    }

    await updateUserProfile(payload)
    const latest = await fetchGetUserInfo()
    userStore.setUserInfo(latest)
    ElMessage.success('Profile updated')
    isEdit.value = false
  }

  const editPwd = () => {
    isEditPwd.value = !isEditPwd.value
  }
</script>
